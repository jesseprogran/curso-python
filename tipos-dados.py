"""
    Tipos de Dados
    
    int - inteiro
    float - decimal
    str - string
    bool - boolean (true ou false)
    list - lista
    tuple - tupla
    dict - dicionário
    
"""    


nome = 'jessé'
idade = 32
peso = 111
altura = 1.74
estudante = True

lista = [nome, idade, peso, altura, estudante] #mutáveis
tupla = ('José', 23, 123, 1.43, 'estudante')#são imutáveis

print(lista)
print(tupla)

dicionario = {
    'nome':"Diego",
    'idade': 32,
    'altura': 1.74,
    'peso': 111,
    'estudante':False
}

print(dicionario)